# Variables - no need to declare type
name = "Arjun"          # String
age = 25                # Integer
height = 5.9            # Float
is_student = True       # Boolean
nothing = None          # NoneType

# Multiple assignment
x, y, z = 10, 20, 30
a = b = c = 100

# Type checking
print(type(name))       # <class 'str'>
print(type(age))        # <class 'int'>

# Type conversion
num_str = "123"
num_int = int(num_str)  # String to Integer
pi = 3.14
pi_int = int(pi)        # Float to Integer (3)