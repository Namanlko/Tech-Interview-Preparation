# Problem 3: Arithmetic Operations: Take two numbers, perform all arithmetic operations.

a = int(input("Enter First Number :)"))
b = int(input("Enter Second Number :)"))

print("a + b = ", (a+b))
print("a - b = ", (a-b))
print("a / b = ", (a/b))
print("a * b = ", (a*b))
print("a % b = ", (a%b))
print("a ** b = ", (a**b))